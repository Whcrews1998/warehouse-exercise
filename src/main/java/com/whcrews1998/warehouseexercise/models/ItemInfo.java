package com.whcrews1998.warehouseexercise.models;

public class ItemInfo {
    private int id;
    private String name;

    public ItemInfo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
